bool ascending(int x, int y) {
    return x>y;
}
bool descending(int x, int y) {
    return x<y;
}
bool isEqual(int x, int y){
    return x==y;
}
bool isNotEqual(int x, int y) {
    return !isEqual(x, y);
}
